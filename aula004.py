# Tipos int e float

# int -> número inteiro
# O tipo int representa qualquer número
# positivo ou negativo. int sem sinal é considerado
# positivo
print(10) #int
print(-10) #int
print(0) #int

# float -> número real ou número com ponto flutuante
# O tipo float apresenta qualquer número 
# positivo ou negativo com ponto flutuante.
# float sem sinal é considerado positivo.
print(7.5)#float
print(-7.5)#float
print(0.0)#float

# A função type mostra o tipo que o Python
# inferiu ao valor.
print(type('Luiz'))
print(type(1), type(7.5))

